#include "libft.h"

int		ft_atoi(const char *str)
{
	int c;
	int k;
	int p;

	c = 0;
	k = 0;
	p = 0;
	while (str[c] == '\n' || str[c] == '\t' || str[c] == '\v' || str[c] == ' '
			|| str[c] == '\f' || str[c] == '\r')
		c++;
	if (str[c] == '-' || str[c] == '+')
	{
		if (str[c] == '-')
			p = 1;
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		k *= 10;
		k += str[c] - 48;
		c++;
	}
	if (p == 1)
		k *= -1;
	return (k);
}
